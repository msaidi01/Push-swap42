#include "checker.h"

void	ft_error(void)
{
	write(2, "Error\n", 6);
	exit(1);
}

int	ft_atoi(const char *n)
{
	int		i;
	long	r;
	int		s;

	i = 0;
	r = 0;
	s = 1;
	if ((n[i] == '+' || n[i] == '-') && (n[i + 1] >= '0' && n[i + 1] <= '9'))
	{
		if (n[i] == '-')
			s *= (-1);
		i++;
	}
	while (n[i] >= '0' && n[i] <= '9')
		r = r * 10 + (n[i++] - '0');
	if (n[i])
		ft_error();
	r = r * s;
	if (r < -2147483648 || r > 2147483647)
		ft_error();
	return (r);
}
