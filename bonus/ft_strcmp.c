#include "checker.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	unsigned char	*ps1;
	unsigned char	*ps2;
	int				i;

	ps1 = (unsigned char *)s1;
	ps2 = (unsigned char *)s2;
	i = 0;
	while (ps1[i] == ps2[i] && ps1[i] && ps2[i])
		i++;
	return (ps1[i] - ps2[i]);
}
